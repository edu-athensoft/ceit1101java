package com.athensoft.session1.basics;

/**
 * Activity - Primitive data types
 * @author ildmost
 *
 */
public class PrimitiveType {

	public static void main(String[] args) {
		short a = 125;
		System.out.println("short type, a="+a);
		
		byte b = 125;
		System.out.println("byte type, b="+b);
		
		int c = 125;
		System.out.println("int type, c="+c);
		
		long d = 125L;
		System.out.println("int type, d="+d);

	}

}
