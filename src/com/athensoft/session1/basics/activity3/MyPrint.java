public class MyPrint {
	public static void main(String[] args) {
		 System.out.print("Hello, ");
		 System.out.print("Athens!");
		 System.out.print("\n");
		 
		 System.out.println("Hello, Athens!");
		 System.out.println();
		 
		 
		 System.out.printf("Hello, %s!\n","Athens");
	}
}
